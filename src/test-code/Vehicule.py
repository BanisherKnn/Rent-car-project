class Vehicule:
    def __init__(self, vehicule_id, marque, model, categorie, portes, etat, tarif):
        self.marque = marque
        self.vehicule_id = vehicule_id
        self.model = model
        self.categorie = categorie
        self.portes = portes
        self.etat = etat
        self.tarif = tarif
